/***************************** Include files *******************************/
#include <stdint.h>

/*****************************    Defines    *******************************/

/*****************************   Variables   *******************************/
volatile uint32_t _ticks = 0;
/*****************************   Functions   *******************************/


void vApplicationTickHook( void )
{
  _ticks++;
}

uint32_t ticks( void )
{
  return _ticks;
}

